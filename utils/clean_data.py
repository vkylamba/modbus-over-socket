    count = 0
    min_count = 5
    add_this_char = False
    for idx, x in enumerate(data):
        if x == u'\x00':
            if start_idx is None:
                start_idx = idx
            count += 1

            if count < min_count:
                add_this_char = True

        else:
            add_this_char = True

        if add_this_char:
            new_data += x

        add_this_char = False

    return new_data


